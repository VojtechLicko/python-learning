# Advanced Numbers

# Hexadecimal

print(hex(12648430))
print(bin(12648430))

# Exponentials
# The function pow() takes two arguments, equivalent to x^y.
# With three arguments it is equivalent to (x^y)%z,
# but may be more efficient for long integers.
print(pow(2, 4,))


# Absolute value
print(abs(abs(-3.14)))

# Round
# The function round() will round a number to a given precision in decimal
#  digits (default 0 digits). It does not convert integers to floats.
print(round(395, -2))
print(round(3.1415926535, 2))

