b = "Hello, World!"
print(b[2:5])  # will return characters from character 3 to character
# 5 (including the first num but excluding the second)
b = "Hello, World!"
print(b[2:])  # will return everything from character 3
# Negative indexing
b = "Hello, World!"
print(b[-5:-2])  # Get the characters:

# From: "o" in "World!" (position -5)
# start stop step
# To, but not included: "d" in "World!" (position -2):
