# int means integer and it basically stands for number with decimal point
x = 5.99
# int function in its core basically floors the number so it does not have any decimal point
print(int(x))
y = 5
# float basically is number with decimal point
print(float(y))
# so you can use both these functions to convert numbers between each other.

# Float can also be scientific numbers with an "e" to indicate the power of 10.
x = 35e3
print(x)
